#include "libft.h"

int	ft_isalnum(int c)
{
	if (((97 <= c) && (c <= 122)) || ((65 <= c) && (c <= 90))
	|| ((c <= 57) && (c >= 48)))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
